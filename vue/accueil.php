                            <div class="card-text fw-bold" style="color: #191970; font-size: 12pt; letter-spacing: 1px;">
                                <h5 class="mb-2" style="color: #191970;"><b>Introduction</b></h5>
                                <p class="card-text text-justify" style="color: #191970; font-size: 12pt; letter-spacing: 1px;">Le BTS SIO <small>(Services Informatiques aux Organisations)</small> a été créer en septembre 2011 afin de remplacer le BTS Informatique de Gestion. Le BTS SIO répond plus que jamais aux besoins du marché du travail, grâce aux compétences acquises, parfaitement adaptées au besoins des entreprise en informatique.</p>
                                <br>
                                <h5 class="mb-2" style="color: #191970;"><b>Les options</b></h5>
                                <p class="card-text" style="color: #191970; font-size: 12pt; letter-spacing: 1px;">Le BTS SIO possède <u>deux options</u> :</p>
                                <ul>
                                    <li class="text-justify" style="color: #191970;">
                                        <b>SISR</b> <small>(Solutions d'Infrastructures, Système et Réseaux)</small> : permet à son détenteur de mettre en place et de gérer des infrastructures réseaux au sein d'une organisation.
                                    </li>
                                    <li class="text-justify" style="color: #191970;">
                                        <b>SLAM</b> <small>(Solutions Logicielles et Application Métiers)</small> : forme des professionnels capables de concevoir des programmes destinées à la gestion d'une organisation.
                                    </li>
                                </ul>
                                <br>
                                <h5 class="mb-2" style="color: #191970;"><b>Matières étudiées</b></h5>
                                <ul>
                                    <li style="color: #191970;">Expression et communication de langue anglais</li>
                                    <li style="color: #191970;">Culture Général et expression</li>
                                    <li style="color: #191970;">Mathématiques</li>
                                    <li style="color: #191970;">Algorithmique appliqués à l'informatique</li>
                                    <li style="color: #191970;">Économie et Management</li>
                                    <li style="color: #191970;">Droit de l'informatique</li>
                                </ul>
                                <p class="card-text text-center" style="color: #191970; font-size: 12pt; letter-spacing: 1px;">
